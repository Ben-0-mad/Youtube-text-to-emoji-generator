def libtotemplate(library: dict) -> dict:
    '''converts tsukimoji into templates'''
    template = {}
    for letter, moonletter in library.items():
        output = ""
        for emoji in moonletter:
            if emoji == '🌑':
                output = output + "1"
            if emoji == '🌒':
                output = output + "2"
            if emoji == '🌓':
                output = output + "3"
            if emoji == '🌔':
                output = output + "4"
            if emoji == '🌕':
                output = output + "5"
            if emoji == '🌘':
                output = output + "6"
            if emoji == '🌗':
                output = output + "7"
            if emoji == '🌖':
                output = output + "8"
            if emoji.isspace() == True:
                output = output + "0"
        template[letter] = output
    return template




def templatetolib(template: dict, base: dict) -> dict:
    '''converts a template into emoji libaries'''
    library = {}
    for letter, moonletter in template.items():
        output = "\n"
        for emoji in moonletter:
            if emoji == '1':
                output = output + base[1]
            if emoji == '2':
                output = output + base[2]
            if emoji == '3':
                output = output + base[3]
            if emoji == '4':
                output = output + base[4]
            if emoji == '5':
                output = output + base[5]
            if emoji == '6':
                output = output + base[6]
            if emoji == '7':
                output = output + base[7]
            if emoji == '8':
                output = output + base[8]
            if emoji == '0':
                output = output + "\n"
        library[letter] = output
    return library




